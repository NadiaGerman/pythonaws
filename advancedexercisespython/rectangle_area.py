# rectangle_area.py

def rectangle_area(length, width):
    """Calculate the area of a rectangle."""
    return length * width

# Testing the function with different inputs
print("Area of 5 x 10 rectangle:", rectangle_area(5, 10))
print("Area of 3.2 x 7.5 rectangle:", rectangle_area(3.2, 7.5))
print("Area of 0 x 9 rectangle:", rectangle_area(0, 9))
print("Area of 8 x 8 rectangle:", rectangle_area(8, 8))
